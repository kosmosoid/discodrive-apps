export const locale = ref('en')
export const languages = [
  { code: 'en', label: 'English' },
  { code: 'de', label: 'Deutsch' },
  { code: 'uk', label: 'Українська' },
  { code: 'fr', label: 'Français' },
  { code: 'es', label: 'Español' },
  { code: 'ru', label: 'Русский' },
  { code: 'sr', label: 'Српски' },
]

export function setLocale(l) {
  if (messages[l]) locale.value = l
}

// t reads locale.value so callers are reactive; supports {name}-style params.
export function t(key, params) {
  const dict = messages[locale.value] || messages.en
  let s = dict[key] ?? messages.en[key] ?? key
  if (params) {
    for (const k in params) s = s.replaceAll(`{${k}}`, params[k])
  }
  return s
}
